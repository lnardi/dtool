    public void operationControl(int op, boolean status, String[] list) {
        switch (op) {
            case DtoolJFrame.OP_SERVER_CONNECTION: //Login
                jButton5.setEnabled(false);
                jButton6.setEnabled(false);
                break;
            case DtoolJFrame.OP_LOGIN: //Login
                jButton5.setEnabled(status);
                jButton6.setEnabled(status);
                break;
            case DtoolJFrame.OP_DUMP: //dump
                if (status) {
                    dumpEvent(list[0]);
                }
                break;
        }
    }

    public void dumpEvent(String id) {
        jTextField2.setText(id);
        dump();

    }

    public void dump() {
        DtoolLogControl.log("Dumping:  " + jTextField2.getText(), Level.INFO);
        jTextArea3.setText("Processando!!!!");
        jButton6.setEnabled(false);
        jButton5.setEnabled(false);
        jButton1.setEnabled(false);
        jTextField2.requestFocus();

        SwingWorker worker = new SwingWorker<Void, Integer>() {
            @Override
            protected Void doInBackground() throws Exception {
                jTextArea3.setText(DtoolDqlControl.apiexec(jTextField2.getText()));

                return null;
            }

            @Override
            protected void done() {
                jButton6.setEnabled(true);
                jButton5.setEnabled(true);
                jButton1.setEnabled(true);
                jTextArea3.setCaretPosition(0);
                dtoolJFrame.operationControl(dtoolJFrame.OP_DUMP, false, null);//remove a barra de progresso          
                jTextField1.requestFocus();
            }

        };
        worker.execute();

    }

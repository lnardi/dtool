   private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int rows[] = jTable1.getSelectedRows();

       if (rows.length == 0) {
           DtoolLogControl.log("Selecione uma lenha para exclus√£o", Level.WARNING);
           return;
       }

       QueryTableModel model = (QueryTableModel) jTable1.getModel();
       DtoolQueryControl.removeQuerys(rows, model);
       DtoolQueryControl.loadQueries(jTable1, (String) jComboBox2.getSelectedItem());
       rowSorter = new TableRowSorter(jTable1.getModel());
       jTable1.setRowSorter(rowSorter);
   }//GEN-LAST:event_jButton2ActionPerformed


    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        //Permite o dump
        if (keyPressed) {
            keyPressed = false;
            int row = jTable1.getSelectedRow();
            int col = jTable1.getSelectedColumn();

            String id = (String) jTable1.getModel().getValueAt(row, col);
            if (DocumentumUseful.validId(id)) {
                dtoolJFrame.operationControl(dtoolJFrame.OP_DUMP, true, new String[]{id});
            } else {
                DtoolLogControl.log("Dump de ID Inválido!!!", Level.SEVERE);
            }

        }

        //Faz a cópia
        if (evt.getClickCount() == 2) {

            int row = jTable1.getSelectedRow();
            int col = jTable1.getSelectedColumn();

            // Clipboard cb = Toolkit.getDefaultToolkit().getSystemClipboard();
            //  cb.setContents(new CellTransferable(jTable1.getValueAt(row, col)), null);
            StringSelection selection = new StringSelection((String) (jTable1.getValueAt(row, col)));
            Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
            clipboard.setContents(selection, selection);
        }


    }//GEN-LAST:event_jTable1MouseClicked

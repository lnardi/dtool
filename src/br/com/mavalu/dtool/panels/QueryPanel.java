    /**
     *
     * Metodo utilizado para execução de ações entre os difernetes componentes
     * de interface
     *
     * @param op - Operação: SERVER_CONNECTION = 0; LOGIN = 1; LOGIN_CREDENTIALS
     * = 2; PROGRESS_BAR = 3; DUMP = 4;
     * @param status Flag que seta o status nos componentes;
     * @param list Array de String com valores que devem ser tratados em cada
     * status. Não é feito um controle de tamaho
     */
    public void operationControl(int op, boolean status, String[] list) {
        switch (op) {
            case DtoolJFrame.OP_SERVER_CONNECTION: //Login
                jButton2.setEnabled(false);
                jTextField3.setEditable(false);
                jButton10.setEnabled(false);
                jButton11.setEnabled(false);
                jButton12.setEnabled(false);
                jButton13.setEnabled(false);
                jButton2.setEnabled(false);
                jButton7.setEnabled(false);
                jButton8.setEnabled(false);
                jButton9.setEnabled(false);
                jComboBox2.setEnabled(false);

                break;
            case DtoolJFrame.OP_LOGIN: //Login
                jButton2.setEnabled(status);
                jTextField3.setEditable(status);
                jComboBox2.setEnabled(status);
                jButton7.setEnabled(true);
                break;

            case DtoolJFrame.OP_SET_QUERY:
                jTextArea2.setText(list[0]);
                break;

            case DtoolJFrame.OP_EXPORT:
                jButton2.setEnabled(status);
                jTextField3.setEditable(status);
                jComboBox2.setEnabled(status);
                jButton7.setEnabled(status);
                jButton10.setEnabled(status);
                break;
        }
    }

